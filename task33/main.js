let a=parseInt(prompt("Enter a number"));
let b=parseInt(prompt("Enter a number"));
let op=prompt("Enter a operator");
switch(op){
    case '+':console.log("The add is "+(a+b));break;
    case '-':console.log("The sub is "+(a-b));break;
    case '*':console.log("The mul is "+(a*b));break;
    case '/':console.log("The div is "+(a/b));break;
}
